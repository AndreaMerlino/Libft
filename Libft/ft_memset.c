#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int			n;
	char		*p;

	n = 0;
	p = b;
	while (n < len)
	{
	*p = (unsigned char) c;
	p++;
	n++;
	}
	if (*(p -1) != '\0')
		*p = '\0';
	return (p);
}
