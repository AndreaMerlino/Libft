#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	i;

	i = 0;
	while (*s1 && *s2 && i < n -1)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		else
		{
			s1 ++;
			s2 ++;
			i ++;
		}
	}
	return (*s1 - *s2);
}
